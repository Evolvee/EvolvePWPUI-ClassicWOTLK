local MAIMISSINGREALMS = {}
function MAIGetRealmIDByName(name)
	if MAIRealmsByName[name] == nil then
		name = name:gsub(" ", "")
		if MAIRealmsByName[name] ~= nil then
			return MAIRealmsByName[name]
		end
		if MAIMISSINGREALMS[name] == nil and name and name ~= "" then
			MAIMISSINGREALMS[name] = true

			local realmID = nil
			if false and name == GetRealmName() then
				local guid = UnitGUID("PLAYER")
				if guid then
					_, realmID, _ = strsplit("-", guid)
				end
			else
				for i, v in pairs(REALMSUNITTEST) do
					if not realmID then
						local guid = UnitGUID(v)
						if guid then
							_, realmID, _ = strsplit("-", guid)
						end
					end
				end
			end

			if not realmID then
				realmID = "???"
			end

			MAIMSG("Send it to the Developer")
			MAIMSG("Missing Realm: " .. name .. " (ID: " .. realmID .. ")")
			MAIMSG("Send it to the Developer")
		end
		return name
	end
	return MAIRealmsByName[name]
end

function MAIGetRealmLangByID(id)
	if MAIRealms[id] then
		local lang = MAIRealms[id]
		if lang then
			local s, e = string.find(lang, ",")
			lang = string.sub(lang, s + 1)

			local s2, e2 = string.find(lang, ",")
			if e2 then
				lang = string.sub(lang, 0, e2 - 1)
			end
		end
		return lang
	end
	return nil
end



-- TOOLTIPS
function MAIGetFlagString(name)
	local server = ""
	local s, e = string.find(name, "-")
	if s then
		server = strsub(name, s + 1)
	else
		server = GetRealmName()
	end
	local lang = MAIGetRealmLangByID(MAIGetRealmIDByName(server))
	if lang then
		return "|T".."Interface\\Addons\\D4KiR MoveAndImprove\\media\\" .. lang .. ":12:24:0:0|t"
	end
	return ""
end

function MAIAddFlag(tt, name)
	if MAIBUILD ~= "RETAIL" and not UnitInBattleground("PLAYER") then
		return
	end
	if MAIGV( "GameTooltip" .. SELL_PRICE ) and MAIGetFlagString(name) ~= "" then
		tt:AddLine(getglobal("LANGUAGE") .. ": " .. MAIGetFlagString(name))
	end
end

