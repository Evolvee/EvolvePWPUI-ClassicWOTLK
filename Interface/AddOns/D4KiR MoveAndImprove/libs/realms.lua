MAIRealmsByName = {}
for i, v in pairs(MAIRealms) do
	local realmtab = { string.split(",", v) }

	local realmName = realmtab[1]
	local realmLang = realmtab[2]
	local realmCoun = realmtab[3]

	realmName = realmName:gsub(" ", "")

	local regionID = GetCurrentRegion()

	local regions = {}
	regions[1] = "US"
	regions[2] = "KR"
	regions[3] = "EU"
